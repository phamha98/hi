class Student {
    private String name;
    private int age;
    private double gpa;
 
    public String getName() {
       return name;
    }
 
    public void setName(String name) {
       this.name = name;
    }
 
    public int getAge() {
       return age;
    }
 
    public void setAge(int age) {
       this.age = age;
    }
 
    public double getGpa() {
       return gpa;
    }
 
    public void setGpa(double gpa) {
       this.gpa = gpa;
    }
 
 }